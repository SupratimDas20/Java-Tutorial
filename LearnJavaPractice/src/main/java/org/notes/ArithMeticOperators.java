package org.notes;

public class ArithMeticOperators {
    public static void main(String[] args){

//        Arithmetic operator: +, -, *, /, %.
        int x=10;
        int y=3;
        int z;

        z=x+y;
        System.out.println(z);

        z=x-y;
        System.out.println(z);

        z=x*y;
        System.out.println(z);

        z=x/y;
        System.out.println(z);

        z=x%y;
        System.out.println(z);
    }
}

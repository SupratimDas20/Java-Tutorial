package org.notes;

public class AugAssignmentOperator {

    public static void main(String[] args){
        double x=10;
        double y=3;

        x+=y;     //x=x+y;
        System.out.println(x);

        x-=y;   //x=x-y;
        System.out.println(x);

        x*=y;   //x=x*y;
        System.out.println(x);

        x/=y;   //x=x/y;
        System.out.println(x);

        x%=y;   //x=x%y;
        System.out.println(x);


    }
}
